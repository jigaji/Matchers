import sortByHealth from '../sortByHealth';

test('sort by health', () => {
  const players = [
    { name: 'мечник', health: 10 },
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
  ];
  const expected = [
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
    { name: 'мечник', health: 10 },
  ];
  const result = sortByHealth(players);
  expect(result).toEqual(expected);
});
